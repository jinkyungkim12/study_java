package com.myclass101.study.ch06;

public class CarThird {

	
	String name = "아반떼";
	int cc = 2000;
	String color = "green";
	
	
	CarThird() {
	
	}
	
	CarThird(String name) {
		this.name = name;
	}
	
	CarThird(String name, int cc) {
		this.name = name;
		this.cc = cc;
	}
	
	CarThird(String name, int cc, String color) {
		this.name = name;
		this.cc = cc;
		this.color = color;
	}
	
}
